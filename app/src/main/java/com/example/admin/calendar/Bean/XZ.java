package com.example.admin.calendar.Bean;



public class XZ {

    private String yunshi;
    private String luckNum;
    private String luckColor;
    private String luckXZ;
    private String luckAspect;
    private String luckTime;

    public XZ(){

    }

    public String getYunshi() {
        return yunshi;
    }

    public void setYunshi(String yunshi) {
        this.yunshi = yunshi;
    }

    public String getLuckNum() {
        return luckNum;
    }

    public void setLuckNum(String luckNum) {
        this.luckNum = luckNum;
    }

    public String getLuckColor() {
        return luckColor;
    }

    public void setLuckColor(String luckColor) {
        this.luckColor = luckColor;
    }

    public String getLuckXZ() {
        return luckXZ;
    }

    public void setLuckXZ(String luckXZ) {
        this.luckXZ = luckXZ;
    }

    public String getLuckAspect() {
        return luckAspect;
    }

    public void setLuckAspect(String luckAspect) {
        this.luckAspect = luckAspect;
    }

    public String getLuckTime() {
        return luckTime;
    }

    public void setLuckTime(String luckTime) {
        this.luckTime = luckTime;
    }

    @Override
    public String toString() {
        return "XZ [yunshi=" + yunshi + ", luckNum=" + luckNum + ", luckColor=" + luckColor + ", luckXZ=" + luckXZ
                + ", luckAspect=" + luckAspect + ", luckTime=" + luckTime + "]";
    }

}
